    private void BtnRendirseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnRendirseMouseClicked
        // TODO add your handling code here:
        if (lblTurno.getText().equals("Rojo")) {

            int confirmar = JOptionPane.showConfirmDialog(null, mas.UsuarioActual.getUser() + "\n¿Estas Seguro que Quieres Rendirte? \nEsto hara que pierdas la Partida", "Atencion", JOptionPane.OK_CANCEL_OPTION);
            if (confirmar == 0) {
                VictoriaNegra();
            }
        } else {
            if (lblTurno.getText().equals("Negro")) {

                int confirmar = JOptionPane.showConfirmDialog(null, mas.UsuarioRival.getUser() + "\n¿Estas Seguro que Quieres Rendirte? \nEsto hara que pierdas la Partida", "Atencion", JOptionPane.OK_CANCEL_OPTION);
                if (confirmar == 0) {
                    VictoriaRoja();
                }
            }
        }
        
    }//GEN-LAST:event_BtnRendirseMouseClicked

